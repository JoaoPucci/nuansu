// Syncs server-side env vars from the canonical .env source (XDG by default)
// into apps/web/.dev.vars, which `wrangler pages dev` reads to populate the
// worker's c.env binding.
//
// Re-run any time the source file changes:
//
//   pnpm sync-env
//
// `apps/web/.dev.vars` is git-ignored. The file is written with mode 0600.

import { chmodSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parseEnvFile, resolveEnvPath } from "./load-env.mjs";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(SCRIPT_DIR, "..");
const DEV_VARS = join(REPO_ROOT, "apps", "web", ".dev.vars");

const sourcePath = resolveEnvPath();
if (!sourcePath) {
  console.error("[sync-wrangler-vars] no source env file found; skipping (this is fine before A1)");
  process.exit(0);
}

const parsed = parseEnvFile(readFileSync(sourcePath, "utf8"));

const lines = [
  "# Generated by scripts/sync-wrangler-vars.mjs — do not edit by hand.",
  `# Source: ${sourcePath}`,
  `# Regenerate: pnpm sync-env`,
  "",
];
for (const [k, v] of Object.entries(parsed)) {
  if (k.startsWith("VITE_")) continue;
  const needsQuotes = /[\s#'"]/.test(v);
  lines.push(`${k}=${needsQuotes ? JSON.stringify(v) : v}`);
}

writeFileSync(DEV_VARS, lines.join("\n") + "\n", "utf8");
try {
  chmodSync(DEV_VARS, 0o600);
} catch {
  // chmod may not be honoured on some filesystems; non-fatal.
}
console.error(`[sync-wrangler-vars] wrote ${DEV_VARS} from ${sourcePath}`);
